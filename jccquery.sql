CREATE TABLE movies (
    name VARCHAR(255) PRIMARY KEY,
	author varchar(255) not null,
    category VARCHAR(100),
    duration INT, -- Duration in minutes
	subtitles varchar(100),
	nationality varchar(100)
);

CREATE TABLE programs (
    programme_name VARCHAR(255) PRIMARY KEY,
    duration INT
);

CREATE TABLE programs_movies (
    programme_name VARCHAR(255) REFERENCES programs(programme_name) ON DELETE CASCADE,
    movie_name VARCHAR(255) REFERENCES movies(name) ON DELETE CASCADE,
    PRIMARY KEY (programme_name, movie_name)
);

CREATE TABLE venues (
    name VARCHAR(255) PRIMARY KEY,
    address VARCHAR(255),
    latitude FLOAT,
    longitude FLOAT
);

CREATE TABLE agenda (
    id SERIAL PRIMARY KEY,
    date DATE NOT NULL,
    venue_id VARCHAR(255) REFERENCES venues(name),
    hour TIME NOT NULL,
    movie_or_programme_name VARCHAR(255)
);

